package me.hossain.ebrahim.operators;

public class ArithmeticOperators {

    public static void main(String[] args) {
        // Initialize two variables
        int x = 100, y = 50;

        // Perform arithmetic operations
        System.out.println("Used '+' operator : " + (x + y));  // Output: 150
        System.out.println("Used '-' operator : " + (x - y));  // Output: 50
        System.out.println("Used '*' operator : " + (x * y));  // Output: 5000
        System.out.println("Used '/' operator : " + (x / y));  // Output: 2
        System.out.println("Used '%' operator : " + (x % y));  // Output: 0
    }
}
